
var color = "Red";

switch (color){

    case "White":
        console.log("Color is white");
        break;
    case "Blue":
        console.log("Color is Blue");
        break;
    case "Black":
        console.log("Color is Black");
        break;
    case "Red":
        console.log("Color is Red");
        break;
    case "Yellow":
        console.log("Color is Yellow");
        break;
    default:
        console.log("Color not found");
}
    