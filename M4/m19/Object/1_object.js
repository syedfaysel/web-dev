// Object in JavaScript 


var student = {
    name: "rajo",
    id: 12345678,
    dept: "cse"
}

//===========================
//printing an object
console.log(student);

//dot notation to access object 
console.log(student.name);

// Bracket notation to access object 
console.log(student["name"]);

console.log(`Name of the Student : ${student.name}, Student ID: ${student.id}`);
